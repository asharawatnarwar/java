import java.util.Scanner;
class Star
{
    public static void main(String arr[])
    {
        int i,j;
        for (i=1;i<=5;i++)
        {
            for (j=3;j<=4;j++)
            {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
