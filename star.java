class Star
{
    public static void main(String arr[])
    {
        int i,j;
        for (i=1;i<=5;i++)
        {
            {
                System.out.print(" ");
            }
            for (j=1;j<=i;j++)
            {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}